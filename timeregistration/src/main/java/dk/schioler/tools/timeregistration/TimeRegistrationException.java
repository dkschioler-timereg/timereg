package dk.schioler.tools.timeregistration;

public class TimeRegistrationException extends RuntimeException {

   private static final long serialVersionUID = 1L;

	public TimeRegistrationException() {

	}

	public TimeRegistrationException(String arg0) {
		super(arg0);

	}

	public TimeRegistrationException(Throwable arg0) {
		super(arg0);

	}

	public TimeRegistrationException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}



}
